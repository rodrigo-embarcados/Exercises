#include <stdio.h>
#include <stdbool.h>

#define N 9
#define M 14

int main() 
{
    bool mudou;
    char gotas[N][M] = 
    {
        {'.', '.', '.', '.', '.', '.', '.', 'o', '.', '.', '.', '.', '.', '.'},
        {'.', '#', '#', '#', '.', '.', '.', '#', '#', '#', '#', '.', '#', '.'},
        {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
        {'.', '.', '#', '#', '#', '#', '#', '#', '.', '.', '.', '.', '.', '.'},
        {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
        {'.', '#', '.', '#', '#', '#', '#', '.', '.', '.', '.', '#', '#', '.'},
        {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
        {'.', '.', '.', '.', '.', '#', '#', '#', '#', '.', '.', '.', '.', '.'},
        {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'}
    };
    printf("Matriz original: \n");
    for(int i = 0; i < N; i++)
    {
        for(int j = 0; j < M; j++)
        {
            printf("%c ", gotas[i][j]);
        }
        printf("\n");
    }
    do
    {
        mudou = false;
        for(int i = 0; i < N; i++)
        {
            for(int j = 0; j < M; j++)
            {
                if(gotas[i][j] == '.')
                {
                    bool c1 = (i - 1 >= 0 && gotas[i - 1][j] == 'o');
                    bool c2 = (j - 1 >= 0 && i + 1 < N && gotas[i][j - 1] == 'o' && gotas[i + 1][j - 1] == '#');
                    bool c3 = (j + 1 < M && i + 1 < N && gotas[i][j + 1] == 'o' && gotas[i + 1][j + 1] == '#');
                    if(c1 || c2 || c3) 
                    {
                        gotas[i][j] = 'o';
                        mudou = true;
                    }
                }
            }
        }
    }
    while(mudou);
    printf("\nMatriz modificada: \n");
    for(int i = 0; i < N; i++)
    {
        for (int j = 0; j < M; j++) 
        {
            printf("%c ", gotas[i][j]);
        }
        printf("\n");
    }
    return 0;
}